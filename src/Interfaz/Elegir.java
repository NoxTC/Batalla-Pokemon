    private void BtnBatallaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnBatallaActionPerformed
        int opcion;
        do {
            // Mostrar opciones
            String[] opciones = {"Empezar una batalla", "Salir"};
            opcion = JOptionPane.showOptionDialog(
                    null,
                    "Elige una opci√≥n:",
                    "Batalla Pok√©mon",
                    JOptionPane.YES_NO_OPTION,
                    JOptionPane.PLAIN_MESSAGE,
                    null,
                    opciones,
                    opciones[0]
            );

            if (opcion == JOptionPane.YES_OPTION) {
                
                batalla.comienza(); // Llama al m√©todo comienza de la clase Batalla
                enBatalla = true;

            }
        } while (opcion != JOptionPane.NO_OPTION);
    }//GEN-LAST:event_BtnBatallaActionPerformed

